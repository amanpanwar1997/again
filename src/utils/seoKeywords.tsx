// Helper function to get keywords for a page
export function getPageKeywords(page: string, subpage?: string): {
  primary: string[];
  lsi: string[];
  local: string[];
  title: string;
  description: string;
} {
  // Handle services pages
  if (page === 'services') {
    if (subpage && seoKeywords.services[subpage as keyof typeof seoKeywords.services]) {
      return seoKeywords.services[subpage as keyof typeof seoKeywords.services] as any;
    }
    return seoKeywords.services.main;
  }

  // Handle industries pages
  if (page === 'industries') {
    if (subpage && seoKeywords.industries[subpage as keyof typeof seoKeywords.industries]) {
      return seoKeywords.industries[subpage as keyof typeof seoKeywords.industries] as any;
    }
    return seoKeywords.industries.main;
  }

  // Handle blogs pages
  if (page === 'blogs') {
    return seoKeywords.blogs.main;
  }
  
  // Handle other pages (home, about, contact, faqs)
  const pageData = seoKeywords[page as keyof typeof seoKeywords];
  if (pageData && 'primary' in pageData) {
    return pageData as any;
  }
  
  // Default to home
  return seoKeywords.home;
}

// Generate all keywords as array for a page
export function getAllKeywords(page: string, subpage?: string): string[] {
  const keywords = getPageKeywords(page, subpage);
  return [...keywords.primary, ...keywords.lsi, ...keywords.local];
}
